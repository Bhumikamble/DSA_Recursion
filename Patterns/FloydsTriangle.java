public class FloydsTriangle {
    public static void main(String[] args){
        int i=5,k=1;
        int n;
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print(k);
                k++;
            }
            System.out.println();
        }
    }
}
